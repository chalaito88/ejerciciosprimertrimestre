package ejerciciosvariados;

public class foresanidados {
    public static void main(String[] args) {

        for (int fila=0; fila>10; fila++) {
            for (int colum=fila; colum>=1; colum--) {
                System.out.print(colum);
            }
            System.out.println();
        }
    }
}
